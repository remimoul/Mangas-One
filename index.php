Demon Slayer (litt. « Pourfendeur de démon »), connue au Japon sous le nom Kimetsu no yaiba (鬼滅の刃?, litt. « La Lame tueuse de démons »), est une série de manga écrite et dessinée par Koyoharu Gotōge. L'histoire suit le périple de Kamado Tanjirō qui cherche un moyen de rendre sa petite sœur Nezuko de nouveau humaine après sa transformation en démon.

Le manga est prépublié dans le magazine Weekly Shōnen Jump de Shūeisha entre février 2016 et mai 2020 et est compilé en un total de 23 tomes. Une première version française est publiée par Panini Manga entre août 2017 et janvier 2018 sous le titre Les Rôdeurs de la nuit ; une nouvelle édition sous le titre actuel par le même éditeur paraît depuis septembre 2019. La série est adaptée en anime par le studio ufotable sous le titre Demon Slayer: Kimetsu no Yaiba. La première saison de 26 épisodes est diffusée initialement entre avril et septembre 2019 et adapte la première saga L'Évolution de Kamado Tanjirō. Elle est suivie d'un film d'animation, intitulé Le Train de l'Infini est sorti en octobre 2020. Une réadaptation du film en sept épisodes est diffusée à partir d'octobre 2021, tandis que la deuxième saison de la série d'animation, adaptant l'arc du Quartier des plaisirs, est diffusée de décembre 2021 à février 2022. Une troisième saison, adaptant l'arc du Village des forgerons, est prévue pour avril 2023. Un jeu vidéo nommé Demon Slayer: Kimetsu no Yaiba - The Hinokami Chronicles est également sorti en octobre 2021.

Demon Slayer est le manga le plus vendu au Japon en 2019 et en 2020. Le tirage total de la franchise a atteint les 150 millions d'exemplaires en février 2021, ce qui classe l'œuvre parmi les séries de mangas les plus vendues de l'histoire. Le manga et son adaptation animée ont été acclamés par la critique. La série animée a reçu de nombreux prix et est considérée comme l'un des meilleurs anime des années 2010.


